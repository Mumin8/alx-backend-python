#!/usr/bin/env python3
'''this will sum a float list'''


def sum_list(input_list: list[float]) -> float:
    '''
    sum_list:
        it will return a float sum of the list

    Args:
      input_list: A list of floats.

    Returns:
      The sum of the floats in the list.
    '''

    return sum(input_list)
